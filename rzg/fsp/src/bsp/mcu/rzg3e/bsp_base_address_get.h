/*******************************************************************************************************************//**
 * Gets a base address that varies depending on the channel or unit.
 *
 * @param      ip       fsp_ip_t enum value for the module to be started
 * @param      ch       The channel or unit.
 * @param      reg      Address per channel or unit.
 **********************************************************************************************************************/
#define R_BSP_BASE_ADDRESS_GET(ip, ch, reg)  (R_BSP_BASE_ADDRESS_GET_ ## ip(ch, reg))

/*******************************************************************************************************************//**
 * Gets a base address of TSU module that varies depending on the channel or unit.
 *
 * @param      ch       The channel or unit.
 * @param      reg      Address per channel or unit.
 **********************************************************************************************************************/
#define R_BSP_BASE_ADDRESS_GET_FSP_IP_TSU(ch, reg)                  \
    {                                                               \
        static const uint32_t volatile * p_tsu_b_base_address[] =   \
        {                                                           \
            [1] = (uint32_t *) R_TSU_B1,                            \
        };                                                          \
                                                                    \
        reg = (R_TSU_B0_Type *) p_tsu_b_base_address[ch];           \
    }

/** @} (end addtogroup BSP_MPU_RZG3E) */

#endif                                 /* BSP_BASE_ADDRESS_GET_H */
